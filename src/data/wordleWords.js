// Function to get daily word based on seed
export function getDailyWord(seed) {
  const seedNum = parseInt(seed, 10);
  const index = seedNum % WORDLE_WORDS.length;
  return WORDLE_WORDS[index];
}

// Function to check if a word is valid
export function isValidWord(word) {
  if (!word || typeof word !== 'string') return false;
  const normalizedWord = word.toLowerCase().trim();
  return normalizedWord.length === 5 && WORDLE_WORDS.includes(normalizedWord);
}

